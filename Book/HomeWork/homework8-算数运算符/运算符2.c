#include<stdio.h>
#include<math.h>

int main()
{
    printf("整形输出：%d\n", 1 + 2.0);
    //1+2.0输出的是1.0+2.0的结果，
    //而你把一个浮点型的数强行写成整数类型自然会出错。
    //!整数型输出为%d，浮点型输出为%f
    printf("浮点型输出：%f\n", 1 + 2.0);
    //正确写法


    //如果你任性非要用整型存储也可以这样
    printf("强行整数输出：%d\n", 1 + (int)2.0);
              //**强制整型要在浮点数前面加(int)

    //再来个反例
    printf("强行1+1.8整数输出：%d\n", 1 + (int)1.8);
    //直接把小数扔了

    //如果后面是(1+0.8)呢
    printf("强行int(1+0.8)整数输出：%d\n", 1 + (int)(1 + 0.8));

    return 0;

}
